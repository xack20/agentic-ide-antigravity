package com.ecommerce.cart.infrastructure.persistence;

import com.ecommerce.cart.domain.valueobjects.ProductId;
import org.springframework.data.annotation.Id;
import org.springframework.data.annotation.Version;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.Document;

import java.time.Instant;
import java.util.HashMap;
import java.util.Map;

@Document(collection = "shopping_carts")
public class CartDocument {
    @Id
    private String cartId;

    @Indexed(unique = true)
    private String guestToken;

    // Map productId -> { quantity }
    // We can store as list or map. Map matches domain.
    private Map<String, Integer> items = new HashMap<>(); // ProductId -> Qty

    @Version
    private Integer version;
    private Instant createdAt;
    private Instant updatedAt;

    public CartDocument() {
    }

    public String getCartId() {
        return cartId;
    }

    public void setCartId(String cartId) {
        this.cartId = cartId;
    }

    public String getGuestToken() {
        return guestToken;
    }

    public void setGuestToken(String guestToken) {
        this.guestToken = guestToken;
    }

    public Map<String, Integer> getItems() {
        return items;
    }

    public void setItems(Map<String, Integer> items) {
        this.items = items;
    }

    public Integer getVersion() {
        return version;
    }

    public void setVersion(Integer version) {
        this.version = version;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public Instant getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Instant updatedAt) {
        this.updatedAt = updatedAt;
    }
}
